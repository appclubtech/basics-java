class ASCIICode {

    public static void main(String[] args) {
        
        // Old fashioned way
        char bigA = 'A';
        int bigAvalue = (int) bigA;
        System.out.println("Value of A: " + bigAvalue);

        // The new way
        int whitespace = ' ';
        System.out.println("Value of a whitespace: " + whitespace);

    }
    
}