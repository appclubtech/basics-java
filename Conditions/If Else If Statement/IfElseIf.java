class IfElseIf {

    public static void main(String[] args) {
        

        int number = 100;

        if (number < 55) {
            System.out.println("Number is less than 55.");
        } else if (number > 23) {
            System.out.println("Number is greater than 23.");
        } else {
            System.out.println("Number is 100");
        }

    }
    
}