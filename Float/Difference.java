class Difference {

    public static void main(String[] args) {
        
        int intValueOne = 1;
        int intValueTwo = 2;
        System.out.println("Integer Result: " + (intValueOne / intValueTwo));

        float floatValueOne = 1.0f;
        float floatValueTwo = 2.0f;
        System.out.println("Float Result: " + (floatValueOne / floatValueTwo));

    }
    
}