class ByteSize {

    public static void main(String[] args) {
        
        byte max = Byte.MAX_VALUE;
        System.out.println("Max value of Byte: " + max);

        byte min = Byte.MIN_VALUE;
        System.out.println("Min value of Byte: " + min);

        byte size = Byte.SIZE;
        System.out.println("Size of Byte: " + size);

    }
}